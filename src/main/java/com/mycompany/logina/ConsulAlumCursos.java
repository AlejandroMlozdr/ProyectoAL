   private void selector() {
    JMenuItem Desasignar = new JMenuItem("Desasignar");
    Desasignar.addActionListener((ActionEvent e) -> {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Cursos cursoSeleccionado = LoginA.curso.get(selectedRow);
            
            Alumno alumnoAEliminar = null;
            for (Alumno a : cursoSeleccionado.Alumnos) {
                if (a.carne.equals(LoginA.alumnoLogeado.carne)) {
                    alumnoAEliminar = a;
                    break;
                }
            }

            if (alumnoAEliminar != null) {
                cursoSeleccionado.Alumnos.remove(alumnoAEliminar); // Elimina al alumno de la lista de alumnos del curso seleccionado
                ActualizarTabla();
                JOptionPane.showMessageDialog(this, "Desasignaci√≥n exitosa.");
            } 
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un curso de la tabla.");
        }
    });

    JPopupMenu popupMenu = new JPopupMenu();
    popupMenu.add(Desasignar);
    jTable1.setComponentPopupMenu(popupMenu);
}

